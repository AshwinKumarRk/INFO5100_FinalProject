      private void populateComboBox(){
          
        OrgComboBox.removeAllItems();
        String ent = enterprise.getEnterpriseType().toString();
         for (Type type : Organisation.Type.values()){
             //String t=type.toString()
              //String o=type.toString();
            if(ent.equals("Grocery Store Unit")){
                 if (type.getValue().equals(Type.GroceryStore.getValue())){
                    OrgComboBox.addItem(type.getValue());
                }
            }else if(ent.equals("Community Fridge Unit")){
                 if (type.getValue().equals(Type.CommunityFridge.getValue())){
                    OrgComboBox.addItem(type.getValue());
                }
               
            }
            else if(ent.equals("Transport Unit")){
                 if (type.getValue().equals(Type.DeliveryMan.getValue())){
                    OrgComboBox.addItem(type.getValue());
                }
              
                 
            }
            else if(ent.equals("Quality Assurance Unit")){
                 if (type.getValue().equals(Type.FoodAdvisor.getValue())){
                    OrgComboBox.addItem(type.getValue());
                }
                if (type.getValue().equals(Type.FoodSafetyOfficer.getValue())){
                    OrgComboBox.addItem(type.getValue());
                } 
            }
         }
         
    }
       private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) OrgTable.getModel();
        
        model.setRowCount(0);
        //System.out.println(Network);
        
        for (Organisation organisation : enterprise.getOrganisationDirectory().getOrganisationList()){
            Object[] row = new Object[2];
            row[0] = organisation.getOrganisationID();
            row[1] = organisation.getName();
            
            model.addRow(row);
        }
    }


    

