    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        // TODO add your handling code here:
         Type type = (Type) OrgComboBox.getSelectedItem();
        String t = String.valueOf(type)+" Organization";
        for(Organisation org: enterprise.getOrganisationDirectory().getOrganisationList()){   
            if(org.getName().equals(t)){
                JOptionPane.showMessageDialog(null, "Organization is already added !","Alert",JOptionPane.INFORMATION_MESSAGE);
                return;
            }
        }
        enterprise.getOrganisationDirectory().createOrganisation(type);
        populateTable();
    }//GEN-LAST:event_AddActionPerformed


