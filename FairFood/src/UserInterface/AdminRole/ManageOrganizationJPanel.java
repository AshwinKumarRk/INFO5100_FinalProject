    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed

        Type type = (Type) OrgCB.getSelectedItem();
        String t = String.valueOf(type)+" Organization";
        for(Organization org: enterprise.getOrganizationDirectory().getOrganizationList()){   
            if(org.getName().equals(t)){
                JOptionPane.showMessageDialog(null, "Organization is added earlier !","Alert",JOptionPane.INFORMATION_MESSAGE);
                return;
            }
        }
        enterprise.getOrganizationDirectory().createOrganization(type);
        populateTable();
        
       
    }//GEN-LAST:event_addActionPerformed

