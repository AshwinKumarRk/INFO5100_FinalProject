    private void populateCombo(){
        OrgCB.removeAllItems();
        String ent = enterprise.getEnterpriseType().toString();
         for (Type type : Organization.Type.values()){
            if(ent.equals("CommunityFridge")){
                 if (type.getValue().equals(Type.CommunityFridge.getValue())){
                    OrgCB.addItem(type);
                }
            }else if(ent.equals("Volunteer")){
                 if (type.getValue().equals(Type.DonationOrganization.getValue())){
                    OrgCB.addItem(type);
                }
                if (type.getValue().equals(Type.EventOrganization.getValue())){
                    OrgCB.addItem(type);
                }
                 
            }
            else if(ent.equals("QualityAssurance")){
                 if (type.getValue().equals(Type.FoodAdvisorOrganisation.getValue())){
                    OrgCB.addItem(type);
                }
                if (type.getValue().equals(Type.FoodSafetyOrganization.getValue())){
                    OrgCB.addItem(type);
                }
              
                 
            }
            else if(ent.equals("GroceryStoreUnit")){
                 if (type.getValue().equals(Type.GroceryStoreOrganization.getValue())){
                    OrgCB.addItem(type);
                }
                if (type.getValue().equals(Type.Delivery.getValue())){
                    OrgCB.addItem(type);
                } 
            }
         }
    }

    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) OrgTable.getModel();
        model.setRowCount(0);        
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            Object[] row = new Object[2];
            row[0] = organization.getOrganizationID();
            row[1] = organization.getName();
            
            model.addRow(row);
        }
    }
